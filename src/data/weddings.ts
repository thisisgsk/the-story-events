export function getWeddingBySlug(slug: string): Wedding | undefined {
  return weddings.find((w) => w.slug === slug);
}

export function getWeddingsByType(type: string): Wedding[] {
  if (type === 'All') return weddings;
  return weddings.filter((w) => w.type === type);
}

export function getWeddingsByCity(city: string): Wedding[] {
  if (city === 'All') return weddings;
  return weddings.filter((w) => w.city.toLowerCase() === city.toLowerCase());
}
